

select * from AOV010 where AOV_DESSEG like '%PES%'

SELECT * FROM SA1010 WHERE A1_CODSEG = '000152'


SELECT A1_COD, A1_NOME, A1_CGC, A1_VEND,A3_NOME, A1_EMAIL FROM SA1010 SA1
INNER JOIN SA3010 ON A3_COD = A1_VEND AND LEFT(A1_FILIAL,4) = A3_FILIAL
WHERE A1_CODSEG = '000152' AND A1_FILIAL = '0101'


SELECT US_COD, US_NOME, US_CGC, US_VEND,A3_NOME, US_EMAIL FROM SUS010 SUS
INNER JOIN SA3010 ON A3_COD = US_VEND AND LEFT(US_FILIAL,4) = A3_FILIAL
WHERE US_CODSEG = '000152' AND US_FILIAL = '0101'


SELECT DISTINCT A1_COD AS CODIGO, A1_NOME AS NOME, A1_CGC AS CNPJ, FT_PRODUTO AS COD_PRODUTO, B1_DESC AS DESCRICAO, MAX(CONVERT(VARCHAR(10), CONVERT(DATE,F2_EMISSAO,103),103)) AS DATA_COMPRA, A3_NOME AS VENDEDORA, A1_EMAIL AS EMAIL FROM SF2010 SF2
INNER JOIN SA1010 ON A1_COD = F2_CLIENTE AND A1_FILIAL = LEFT(F2_FILIAL, 4)
INNER JOIN SA3010 ON A3_COD = F2_VEND1 AND LEFT(F2_FILIAL,4) = A3_FILIAL
INNER JOIN SFT010 ON FT_NFISCAL = F2_DOC AND FT_FILIAL = F2_FILIAL
INNER JOIN SB1010 ON B1_COD = FT_PRODUTO AND FT_FILIAL = B1_FILIAL
WHERE F2_CLIENTE IN (SELECT A1_COD FROM SA1010 WHERE A1_CODSEG = '000152' AND A1_FILIAL = '0101')
GROUP BY A1_CGC,A1_NOME, FT_PRODUTO,A3_NOME, A1_EMAIL,A1_COD, B1_DESC
ORDER BY A1_CGC,A1_NOME, FT_PRODUTO,A3_NOME, A1_EMAIL,A1_COD, B1_DESC






