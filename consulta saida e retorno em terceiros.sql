	SELECT 
	CONVERT(VARCHAR(10),CONVERT(DATE,D2_EMISSAO,103),103) DATA_SAIDA,
	D2_DOC NOTA_SAIDA,
	D2_COD PRODUTO,
	D2_QUANT QTDE_SAIDA,
	D2_PRCVEN PREÇO_SAIDA,
	D2_TOTAL TOTAL_SAIDA,
	ISNULL(CONVERT(VARCHAR(10),CONVERT(DATE,D1_EMISSAO,103),103),'') DATA_RETORNO,
	ISNULL(D1_DOC,'') NOTA_RETORNO ,
	ISNULL(D1_COD,'') PRODUTO,
	ISNULL(D1_QUANT,'') QTDE_RETORNO,
	ISNULL(D1_VUNIT,'') PREÇO_RETORNO,
	ISNULL(D1_TOTAL,'') TOTAL_RETORNO
		FROM SD2010 D2
	LEFT OUTER JOIN SD1010 D1 ON D1_FILIAL = D2_FILIAL 
	AND D1_NFORI = D2_DOC AND D1_SERIORI = D2_SERIE
	AND D1_ITEMORI = D2_ITEM AND D1_COD = D2_COD AND
	D2.D_E_L_E_T_ = '' AND D1.D_E_L_E_T_ = ''
	where D2_CLIENTE ='937029'
	AND D2_FILIAL = '010102'
	AND D2_DOC = '000031351'
	ORDER BY D2_EMISSAO






