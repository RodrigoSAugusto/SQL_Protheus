SELECT H6_PRODUTO, B1_DESC, SUM(H6_QTDPROD) TOTAL, 
		CONVERT(VARCHAR(10),CONVERT(DATE,H6_DATAINI,103),103) AS DATAINI, 
		CONVERT(VARCHAR(10),CONVERT(DATE,H6_DATAFIN,103),103) AS DATAFIM
	FROM SH6010
			INNER JOIN SB1010 SB1 ON B1_COD = H6_PRODUTO AND B1_FILIAL = H6_FILIAL
		WHERE H6_FILIAL = '010102'
			AND H6_RECURSO = 'EXT03'
			AND H6_DATAINI >= '20230201'
			AND H6_DATAFIN <= '20230430'
		GROUP BY H6_PRODUTO, B1_DESC, H6_DATAINI, H6_DATAFIN
		UNION ALL
SELECT 'TOTAL', '', SUM(H6_QTDPROD),'','' 
	FROM SH6010
		WHERE H6_FILIAL = '010102'
