SELECT B1_COD CODIGO, B1_DESC DESCRICAO,D2_CLIENTE COD_CLI,D2_LOJA LOJA, A1_NOME RAZAO_SOCIAL, A1_NREDUZ NOME_FANTAZIA,C5_VEND1 COD_VEND,A3_NOME VENDEDOR,D2_DOC NF, D2_QUANT QTD, D2_EMISSAO DT_EMISSAO
--,* 
FROM SD2010 SD2 (NOLOCK)
JOIN SB1010 SB1 (NOLOCK) ON B1_FILIAL = D2_FILIAL AND  B1_COD = D2_COD
JOIN SA1010 SA1 (NOLOCK) ON A1_FILIAL = SUBSTRING(D2_FILIAL,1,4)  AND A1_COD = D2_CLIENTE AND D2_LOJA = A1_LOJA
JOIN SC5010 SC5 (NOLOCK) ON C5_FILIAL = D2_FILIAL AND C5_NUM = D2_PEDIDO
JOIN SA3010 SA3 (NOLOCK) ON A3_FILIAL = SUBSTRING(D2_FILIAL,1,4) AND A3_COD = C5_VEND1
WHERE 1=1
AND D2_FILIAL='030101'
AND D2_TIPO='N'
AND D2_COD BETWEEN'007340' AND '007340'
AND D2_CLIENTE BETWEEN'' AND 'ZZZZZZ'
AND B1_DESC LIKE '%V7-069%'
ORDER BY D2_EMISSAO
