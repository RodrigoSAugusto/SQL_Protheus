SELECT F2_CLIENTE
,F2_LOJA
,A1_NOME
,A1_EST
,A1_CGC
,SUM(F2_VALFAT)F2_VALFAT
,SUM(F2_VALBRUT)F2_VALBRUT FROM SF2010 SF2
JOIN SA1010 SA1 ON A1_FILIAL =SUBSTRING(F2_FILIAL,1,4) AND A1_COD = F2_CLIENTE	AND A1_LOJA =F2_LOJA and SA1.D_E_L_E_T_='' AND SF2.D_E_L_E_T_=''
WHERE 1=1
--and A1_EST ='MG'
and A1_FILIAL='0101'
GROUP BY F2_CLIENTE
,F2_LOJA
,A1_NOME
,A1_EST
,A1_CGC
--SELECT * FROM SA1010
order by 1,2




