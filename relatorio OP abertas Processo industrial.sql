SELECT 
--DISTINCT D4_OP+D4_LOCAL
--COUNT(DISTINCT D4_OP),D4_OP
--C6_FILIAL,C6_NUM, C6_PRODUTO, B1_DESC,C6_DESCRI, C6_QTDVEN, C6_QTDENT, C6_NUMOP+C6_ITEMOP OP,C6_LOCAL
--DISTINCT 
C6_FILIAL,C6_NUM,C6_PRODUTO,C6_NUMOP+C6_ITEMOP OP,C6_LOCAL,NNR_DESCRI, C5_EMISSAO
--count(distinct C6_NUM) QTD, C6_LOCAL, NNR_DESCRI
--,*
--SD4A.*
FROM SC6010 SC6
JOIN SC5010 SC5 ON C5_FILIAL = C6_FILIAL AND C5_NUM=C6_NUM AND SC5.D_E_L_E_T_=''
JOIN SD4010 SD4A ON SD4A.D4_FILIAL=C6_FILIAL AND SUBSTRING(SD4A.D4_OP,1,8)=C6_NUMOP+C6_ITEMOP AND SD4A.D_E_L_E_T_='' AND SD4A.D4_PRODUTO=SC6.C6_PRODUTO
JOIN SB1010 SB1 ON B1_FILIAL=C6_FILIAL AND C6_PRODUTO = B1_COD AND SB1.D_E_L_E_T_=''
LEFT OUTER JOIN NNR010 NNR ON NNR_FILIAL=C6_FILIAL AND NNR_CODIGO = C6_LOCAL
WHERE 1=1
AND C6_FILIAL='020101'
AND C6_NOTA=''
AND SC6.D_E_L_E_T_=''
AND C6_QTDVEN>C6_QTDENT
AND D4_QUANT >0
--AND D4_OP='00974805001'
--GROUP BY D4_OP,D4_LOCAL
--HAVING COUNT(DISTINCT D4_OP)>1
--GROUP BY C6_LOCAL,NNR_DESCRI
group by C6_FILIAL,C6_NUM,C6_PRODUTO,C6_NUMOP+C6_ITEMOP,C6_LOCAL,NNR_DESCRI,C5_EMISSAO
order by 2

