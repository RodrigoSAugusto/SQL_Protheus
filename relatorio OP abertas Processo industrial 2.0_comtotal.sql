SELECT 
C6_LOCAL, NNR_DESCRI,
count(distinct C6_NUM) QTD
FROM SC6010 SC6
JOIN SC5010 SC5 ON C5_FILIAL = C6_FILIAL AND C5_NUM=C6_NUM AND SC5.D_E_L_E_T_=''
JOIN SB1010 SB1 ON B1_FILIAL=C6_FILIAL AND C6_PRODUTO = B1_COD AND SB1.D_E_L_E_T_=''
LEFT OUTER JOIN SD4010 SD4A ON SD4A.D4_FILIAL=C6_FILIAL AND SUBSTRING(SD4A.D4_OP,1,8)=C6_NUMOP+C6_ITEMOP AND SD4A.D_E_L_E_T_='' AND SD4A.D4_PRODUTO=SC6.C6_PRODUTO
LEFT OUTER JOIN NNR010 NNR ON NNR_FILIAL=C6_FILIAL AND NNR_CODIGO = C6_LOCAL AND NNR.D_E_L_E_T_=''
WHERE 1=1
AND C6_FILIAL='010102'
AND C5_EMISSAO BETWEEN '20100101' AND '20211124'
AND C6_BLQ<>'R'
AND C6_NOTA=''
AND SC6.D_E_L_E_T_=''
and C5_TIPO IN( 'N','I','C','P')
AND C6_QTDVEN>C6_QTDENT
--AND D4_QUANT >0
--AND C6_NUM='009913' 

GROUP BY C6_LOCAL,NNR_DESCRI
order by 1



SELECT 
C6_FILIAL,C6_NUM,C6_ITEM,C6_PRODUTO,A1_COD, A1_LOJA, A1_NOME,C6_NUMOP, C6_ITEMOP,--,D4_OP OP,SD4A.D_E_L_E_T_,
C6_LOCAL,NNR_DESCRI, C5_EMISSAO, C6_QTDVEN VENDIDO, C6_QTDENT FATURADO,C6_QTDVEN -C6_QTDENT RESIDUO,
case when C6_NOTA <>'' then C6_NOTA else C5_NOTA end NOTA
FROM SC6010 SC6
JOIN SC5010 SC5 ON C5_FILIAL = C6_FILIAL AND C5_NUM=C6_NUM AND SC5.D_E_L_E_T_=''
JOIN SB1010 SB1 ON B1_FILIAL=C6_FILIAL AND C6_PRODUTO = B1_COD AND SB1.D_E_L_E_T_=''
JOIN SA1010 SA1 ON SUBSTRING(SC5.C5_FILIAL,1,4) = SA1.A1_FILIAL and  A1_COD = C5_CLIENTE AND A1_LOJA = C5_LOJACLI AND SA1.D_E_L_E_T_=''
--LEFT OUTER JOIN SD4010 SD4A ON SD4A.D4_FILIAL=C6_FILIAL AND SUBSTRING(SD4A.D4_OP,1,8)=C6_NUMOP+C6_ITEMOP  AND SD4A.D4_PRODUTO=SC6.C6_PRODUTO
LEFT OUTER JOIN SC2010 SC2 ON SC2.C2_FILIAL=C6_FILIAL AND C2_NUM=C6_NUMOP AND C2_ITEM=C6_ITEMOP AND C2_PRODUTO=SC6.C6_PRODUTO and SC2.D_E_L_E_T_=''
--AND SD4A.D_E_L_E_T_=''
LEFT OUTER JOIN NNR010 NNR ON NNR_FILIAL=C6_FILIAL AND NNR_CODIGO = C6_LOCAL
WHERE 1=1
AND C6_FILIAL='020101'
--and C6_LOCAL ='06'
AND C5_EMISSAO BETWEEN '20200101' AND '20201229'
AND C6_BLQ<>'R'
and C5_NUM='009748'
and C5_TIPO IN( 'N','I','C','P')
AND SC6.D_E_L_E_T_=''
AND C6_QTDVEN>C6_QTDENT
group by C6_FILIAL,C6_NUM,C6_ITEM,C6_PRODUTO,C6_NUMOP, C6_ITEMOP,--D4_OP,
C6_LOCAL,NNR_DESCRI,C5_EMISSAO,C5_NOTA,C6_NOTA,C6_QTDVEN, C6_QTDENT,A1_COD, A1_LOJA, A1_NOME--,SD4A.D_E_L_E_T_
order by 2




