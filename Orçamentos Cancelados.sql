
-- TRAS DADOS DAS TELASD DE AÇO

SELECT DISTINCT UA_NUM, UA_VEND, UA_CLIENTE,ISNULL(A1_NOME, US_NOME) AS CLIENTE, UB_PRODUTO, UB_XDESNFE, UA_VLRLIQ,YP_SEQ, YP_TEXTO
		FROM SUA010 LEFT JOIN SYP010 ON UA_CODCANC = YP_CHAVE AND YP_CAMPO = 'UA_CODCANC'
		LEFT JOIN SA1010 ON UA_CLIENTE = A1_COD AND LEFT(UA_FILIAL,4) = A1_FILIAL
		LEFT JOIN SUS010 ON UA_CLIENTE = US_COD AND LEFT(UA_FILIAL,4) = US_FILIAL
		INNER JOIN SUB010 ON UA_NUM = UB_NUM AND UA_FILIAL = UB_FILIAL

		WHERE UA_FILIAL = '010101'
		AND UA_NUM IN (	SELECT  DISTINCT UB_NUM
								FROM SUB010
								WHERE UB_FILIAL = '010101'
								AND UB_XDESNFE LIKE '%AÇO%' OR UB_XDESNFE LIKE '% ACO%'
						)
		AND UA_EMISSAO >= '20230601'
		AND UA_EMISSAO <= '20230631'
		AND UA_OPER = '2'
		AND UA_CANC = 'S'
		ORDER BY UA_NUM
		
		--AND UA_NUMSC5 <> ''


