select * from CT2010
WHERE 1=1
AND CT2_FILIAL IN ('030101','030102')
AND (
		(CT2_DEBITO	='21060100004'  and CT2_HIST like '%COMP NCC%')
		OR 
		(CT2_CREDIT  ='11020100002' and CT2_HIST like '%COMP%')
	)
AND CT2_LOTE='008850'
AND CT2_ORIGEM LIKE '%596%'
and D_E_L_E_T_=''
ORDER BY R_E_C_N_O_
