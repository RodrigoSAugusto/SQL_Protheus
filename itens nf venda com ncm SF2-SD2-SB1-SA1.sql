select  D2_DOC NUMERO_NF,D2_ITEM ITEM_NF,D2_EMISSAO EMISSAO, D2_CLIENTE COD_CLI, D2_LOJA LOJA,A1_NOME NOME, B1_TIPO TIPO_PROD,B1_GRUPO GRUPO_PROD, D2_CF CFOP
,D2_COD CODIGO,B1_DESC DESCRICAO,B1_POSIPI NCM, B1_UM UM, D2_QUANT QTD, D2_TOTAL VALOR
from  SD2010 SD2
JOIN SF2010 SF2 ON F2_FILIAL=D2_FILIAL AND F2_DOC=D2_DOC and SD2.D_E_L_E_T_=''
JOIN SB1010 SB1 ON D2_FILIAL = B1_FILIAL AND D2_COD= B1_COD AND SB1.D_E_L_E_T_=''
JOIN SA1010 SA1 ON A1_FILIAL= SUBSTRING(D2_FILIAL,1,4) AND A1_COD=D2_CLIENTE AND A1_LOJA= D2_LOJA AND SA1.D_E_L_E_T_=''
WHERE D2_FILIAL='020101'
AND F2_TIPO ='N'
AND F2_EMISSAO BETWEEN '20200101' AND '20201231'
AND SF2.D_E_L_E_T_=''
--AND F2_DOC='000087047'
ORDER BY SF2.R_E_C_N_O_

