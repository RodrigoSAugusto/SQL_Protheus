SELECT	DISTINCT UB_NUM, 
		UB_PRODUTO, 
		UB_XDESNFE, 
		UB_QUANT, 
		UB_VLRITEM,
		A1_COD,
		A1_NOME,
		A3_COD,
		A3_NOME
	FROM SUB010
	LEFT JOIN SUA010 SUA ON UB_NUM = UA_NUM AND UB_FILIAL = UA_FILIAL
	LEFT JOIN SA1010 SA1 ON UA_CLIENTE = A1_COD AND LEFT(UA_FILIAL,4) = A1_FILIAL
	LEFT JOIN SA3010 SA3 ON UA_VEND = A3_COD AND LEFT(UA_FILIAL,4) = A3_FILIAL
		WHERE	UB_PRODUTO >= '008484'
			AND UB_PRODUTO <= '008484'
			AND A1_COD >= '109028'
			AND A1_COD <= '109028'
			AND A3_COD >= '121062'
			AND A3_COD <= '121062'
			AND UA_EMISSAO >= '20230101'
			AND UA_EMISSAO <= '20231231'
			AND SUA.D_E_L_E_T_ = ''
			AND SA1.D_E_L_E_T_ = ''


