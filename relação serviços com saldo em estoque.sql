select 
B1_FILIAL, B1_COD, B1_DESC, B2_LOCAL, B2_QATU,B2_LOCALIZ
--,*
from SB1010 SB1(NOLOCK)
INNER JOIN SB2010 SB2 (NOLOCK) ON  B1_FILIAL = B2_FILIAL AND B1_COD = B2_COD AND SB1.D_E_L_E_T_='' AND SB2.D_E_L_E_T_=''
WHERE 1=1
AND B1_DESC LIKE '%SERVI%'
AND B1_FILIAL='010101'
AND B2_QATU>0

